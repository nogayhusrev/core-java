package day02_escapeSequences;

public class EscapeSequences {
    public static void main(String[] args) {
        System.out.println("Name:\tDamniel");
        System.out.println("Gender:\tMale");
        System.out.println("Age:\t25");
        System.out.println("JOb Title:\tJav Developer");
        System.out.println("Company:\t\"Apple Inc\"");
        System.out.println("Salary:\t90000");

    }
}
/*
    1. \n:new line
    2. \t: paragraph space
    3. \\:single backslash
    4. \":double quote



 */